export function t(language, key) {
  return DICTIONARY[language]?.[key] || DICTIONARY.en[key] || key;
}

export function statusLabel(language, status) {
  return STATUS_LABELS[language]?.[status] || status;
}

export function fieldLabel(language, fieldName) {
  return FIELD_KEYS[fieldName] ? t(language, FIELD_KEYS[fieldName]) : fieldName;
}

export function confidenceLabel(language, level) {
  if (level === "high") {
    return t(language, "confidenceHigh");
  }
  if (level === "medium") {
    return t(language, "confidenceMedium");
  }
  return t(language, "confidenceLow");
}
